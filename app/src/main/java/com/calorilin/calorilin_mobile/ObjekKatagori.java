package com.calorilin.calorilin_mobile;

public class ObjekKatagori {

    int gambar;
    String namakat;

    ObjekKatagori(int gambar, String namakat){
        this.gambar = gambar;
        this.namakat = namakat;
    }

    public int getGambar() {
        return gambar;
    }

    public String getNamakat() {
        return namakat;
    }
}
